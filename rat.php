									<blockquote class="generic-blockquote">
						Rats are various medium-sized, long-tailed rodents. Species of rats are found throughout the order Rodentia, but stereotypical rats are found in the genus Rattus. Other rat genera include Neotoma (pack rats), Bandicota (bandicoot rats) and Dipodomys (kangaroo rats).
Rats are typically distinguished from mice by their size. Generally, when someone discovers a large muroid rodent, its common name includes the term rat, while if it is smaller, its name includes the term mouse. The common terms rat and mouse are not taxonomically specific. In other words, rat is not a scientific term; if it were, all rats would belong to the genus Rattus
<br>The best-known rat species are the black rat (Rattus rattus) and the brown rat (Rattus norvegicus). This group, generally known as the Old World rats or true rats, originated in Asia. Rats are bigger than most Old World mice, which are their relatives, but seldom weigh over 500 grams (1.1 lb) in the wild. </blockquote><br>
										<h4>
									1. Rat tails
									</h4><br>
                                <img class="img-fluid" src="http://www.hdnicewallpapers.com/Walls/Big/Mouse/White_Rat_With_Cute_Pups_Wallpaper.jpg" alt="" style="height:400px;width:800px;"><br><br>
									<p>
									The characteristic long tail of most rodents is a feature that has been extensively studied in various rat species models, which suggest three primary functions of this structure: thermoregulation, minor proprioception, and a nocifensive-mediated deglovingresponse.[citation needed] Rodent tails—particularly in rat models—have been implicated with a thermoregulation function that follows from its anatomical construction. This particular tail morphology is evident across the family Muridae, in contrast to the bushier tails of Sciuridae, the squirrel family. The tail is hairless and thin skinned but highly vascularized, thus allowing for efficient countercurrent heat exchange with the environment. The high muscular and connective tissue densities of the tail, along with ample muscle attachment sites along its plentiful caudal vertebrae, facilitate specific proprioceptive senses to help orient the rodent in a three-dimensional environment.[ Lastly, murids have evolved a unique defense mechanism termed degloving that allows for escape from predation through the loss of the outermost integumentary layer on the tail. However, this mechanism is associated with multiple pathologies that have been the subject of investigation.<br>
Multiple studies have explored the thermoregulatory capacity of rodent tails by subjecting test organisms to varying levels of physical activity and quantifying heat conduction via the animals' tails. One study demonstrated a significant disparity in heat dissipation from a rat's tail relative to its abdomen.[9] This observation was attributed to the higher proportion of vascularity in the tail, as well as its higher surface-area-to-volume ratio, which directly relates to heat's ability to dissipate via the skin. These findings were confirmed in a separate study analyzing the relationships of heat storage and mechanical efficiency in rodents that exercise in warm environments. In this study, the tail was a focal point in measuring heat accumulation and modulation.[citation needed]<br>
On the other hand, the tail's ability to function as a proprioceptive sensor and modulator has also been investigated. As aforementioned, the tail demonstrates a high degree of muscularization and subsequent innervation that ostensibly collaborate in orienting the organism.[10] Specifically, this is accomplished by coordinated flexion and extension of tail muscles to produce slight shifts in the organism's center of mass, orientation, etc., which ultimately assists it with achieving a state of proprioceptive balance in its environment. Further mechanobiological investigations of the constituent tendons in the tail of the rat have identified multiple factors that influence how the organism navigates its environment with this structure. A particular example is that of a study in which the morphology of these tendons is explicated in detail.[11] Namely, cell viability tests of tendons of the rat's tail demonstrate a higher proportion of living fibroblaststhat produce the collagen for these fibers. As in humans, these tendons contain a high density of golgi tendon organs that help the animal assess stretching of muscle in situ and adjust accordingly by relaying the information to higher cortical areas associated with balance, proprioception, and movement.<br>
The characteristic tail of murids also displays a unique defense mechanism known as degloving in which the outer layer of the integument can be detached in order to facilitate the animal's escape from a predator. This evolutionary selective pressure has persisted despite a multitude of pathologies that can manifest upon shedding part of the tail and exposing more interior elements to the environment.[12]Paramount among these are bacterial and viral infection, as the high density of vascular tissue within the tail becomes exposed upon avulsion or similar injury to the structure. The degloving response is a nocifensive response, meaning that it occurs when the animal is subjected to acute pain, such as when a predator snatches the organism by the tail
</p>
										
										
										
										<h4>
									2. As pets
									</h4><br>
                               
									<p>
							Specially bred rats have been kept as pets at least since the late 19th century. Pet rats are typically variants of the species brown rat - but black rats and giant pouched rats are also sometimes kept. Pet rats behave differently from their wild counterparts depending on how many generations they have been kept as pets.[13] Pet rats do not pose any more of a health risk than pets such as cats or dogs.[14] Tamed rats are generally friendly and can be taught to perform selected behaviors
							</p>	
										
										
										
										<h4>
								3.As subjects for scientific research
									</h4><br>
                               
									<p>
							In 1895, Clark University in Worcester, Massachusetts, established a population of domestic albino brown rats to study the effects of diet and for other physiological studies.[citation needed] Over the years, rats have been used in many experimental studies, adding to our understanding of genetics, diseases, the effects of drugs, and other topics that have provided a great benefit for the health and wellbeing of humankind.<br>
The aortic arches of the rat are among the most commonly studied in murine models due to marked anatomical homology to the human cardiovascular system.[15] Both rat and human aortic arches exhibit subsequent branching of the brachiocephalic trunk, left common carotid artery, and left subclavian artery, as well as geometrically similar, nonplanar curvature in the aortic branches.[15] Aortic arches studied in rats exhibit abnormalities similar to those of humans, including altered pulmonary arteries and double or absent aortic arches.[16] Despite existing anatomical analogy in the inthrathoracic position of the heart itself, the murine model of the heart and its structures remains a valuable tool for studies of human cardiovascular conditions.<br>
Laboratory rats have also proved valuable in psychological studies of learning and other mental processes (Barnett 2002), as well as to understand group behavior and overcrowding (with the work of John B. Calhoun on behavioral sink).[citation needed] A 2007 study found rats to possess metacognition, a mental ability previously only documented in humans and some primates.<br>
Domestic rats differ from wild rats in many ways. They are calmer and less likely to bite; they can tolerate greater crowding; they breed earlier and produce more offspring; and their brains, livers, kidneys, adrenal glands, and hearts are smaller (Barnett 2002).
Brown rats are often used as model organisms for scientific research. Since the publication of the rat genome sequence,[20] and other advances, such as the creation of a rat SNP chip, and the production of knockout rats, the laboratory rat has become a useful genetic tool, although not as popular as mice. When it comes to conducting tests related to intelligence, learning, and drug abuse, rats are a popular choice due to their high intelligence, ingenuity, aggressiveness, and adaptability. Their psychology seems in many ways similar to that of humans.[citation needed]
Entirely new breeds or "lines" of brown rats, such as the Wistar rat, have been bred for use in laboratories. Much of the genome of Rattus norvegicus has been sequenced

</p>	
										
											
										
										<h4>
								4. General intelligence
								</h4><br>
                               
									<p>
							Rat meat is a food that, while taboo in some cultures, is a dietary staple in others.[25][26] Taboos include fears of disease or religious prohibition, but in many places, the high number of rats has led to their incorporation into the local diets.<br>
In some cultures, rats are or have been limited as an acceptable form of food to a particular social or economic class. In the Mishmi culture of India, rats are essential to the traditional diet, as Mishmi women may eat no meat except fish, pork, wild birds and rats.[27] Conversely, the Musahar community in north India has commercialised rat farming as an exotic delicacy.[28] In the traditional cultures of the Hawaiians and the Polynesians, rat was an everyday food for commoners. When feasting, the Polynesian people of Rapa Nui could eat rat meat, but the king was not allowed to, due to the islanders' belief in his "state of sacredness" called tapu.[29] In studying precontact archaeological sites in Hawaii, archaeologists have found the concentration of the remains of rats associated with commoner households accounted for three times the animal remains associated with elite households. The rat bones found in all sites are fragmented, burned and covered in carbonized material, indicating the rats were eaten as food. The greater occurrence of rat remains associated with commoner households may indicate the elites of precontact Hawaii did not consume them as a matter of status or taste.[30]
France has several regions where people consume rat. A recipe for grilled rats, Bordeaux-style, calls for the use of alcoholic rats who live in wine cellars. These rats are skinned and eviscerated, brushed with a thick sauce of olive oil and crushed shallots, and grilled over a fire of broken wine barrels.<br>
Rat stew is consumed in American cuisine in the state of West Virginia.
In France and Victorian Britain rich people ate rat pie.[38] During food rationing due to World War II, British biologists ate laboratory rat, creamed.
Flesh of rat is eaten in Taiwan.<br>
Bandicoot rats are an important food source among some peoples in Southeast Asia, and the United Nations Food and Agriculture Organization estimated rat meat makes up half the locally produced meat consumed in Ghana, where cane rats are farmed and hunted for their meat. African slaves in the American South were known to hunt wood rats (among other animals) to supplement their food rations,[50] and Aborigines along the coast in southern Queensland, Australia, regularly included rats in their diet.[51]
Ricefield rats (Rattus argentiventer) have traditionally been used as food in rice-producing regions such as Valencia, as immortalized by Vicente Blasco Ibáñez in his novel Cañas y barro. Along with eel and local beans known as garrafons, rata de marjal (marsh rat) is one of the main ingredients in traditional paella (later replaced by rabbit, chicken and seafood).[52] Ricefield rats are also consumed in the Philippines, the Isaan region of Thailand, and Cambodia. In late 2008, Reuters reported the price of rat meat had quadrupled in Cambodia, creating a hardship for the poor who could no longer afford it.
Elsewhere in the world, rat meat is considered diseased and unclean, socially unacceptable, or there are strong religious proscriptions against it. Islam and Kashrut traditions prohibit it, while both the Shipibo people of Peru and Sirionó people of Bolivia have cultural taboos against the eating of rats.<br>
Rats are a common food item for snakes, both in the wild, and as pets. Adult rat snakes and ball pythons, for example, are fed a diet of mostly rats in captivity. Rats are readily available (live or frozen) to individual snake owners, as well as to pet shops and reptile zoos, from many suppliers. In Britain, the government prohibited the feeding of any live mammal to another animal in 2007.[citation needed] The rule says the animal must be dead before it is given to the animal to eat. The rule was put into place mainly because of the pressure of the RSPCA and people who said the feeding of live animals was cruel.
</p>	
										
										
								
