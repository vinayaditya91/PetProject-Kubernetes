								5. Working rats
									</h4><br>
								
                               
									<p>
					Rats have been used as working animals. Tasks for working rats include the sniffing of gunpowder residue, demining, acting and animal-assisted therapy.				</p>	
								
								
					<h4>6. For odor detection</h4>
                               
									<p>
				Rats have a keen sense of smell and are easy to train. These characteristics have been employed, for example, by the Belgian non-governmental organization APOPO, which trains rats (specifically African giant pouched rats) to detect landmines and diagnose tuberculosis through smell.</p>	
									
								
							<h4>7. In the spread of disease</h4>
                               
									<p>
					Rats can serve as zoonotic vectors for certain pathogens and thus spread disease, such as bubonic plague, Lassa fever, leptospirosis, and Hantavirus infection.[56]
They are also associated with human dermatitis because they are frequently infested with blood feeding rodent mites such as the tropical rat mite (Ornithonyssus bacoti) and spiny rat mite (Laelaps echidnina), which will opportunistically bite and feed on humans,[57] where the condition is known as rat mite dermatitis
<br></p>							
							<h4>8. As invasive species</h4>
                               
									<p>
					When introduced into locations where rats previously did not exist, they can wreak an enormous degree of environmental degradation. Rattus rattus, the black rat, is considered to be one of the world's worst invasive species.[68] Also known as the ship rat, it has been carried worldwide as a stowaway on seagoing vessels for millennia and has usually accompanied men to any new area visited or settled by human beings by sea. The similar species Rattus norvegicus, the brown rat or wharf rat, has also been carried worldwide by ships in recent centuries.<br>
The ship or wharf rat has contributed to the extinction of many species of wildlife, including birds, small mammals, reptiles, invertebrates, and plants, especially on islands. True rats are omnivorous, capable of eating a wide range of plant and animal foods, and have a very high birth rate. When introduced to a new area, they quickly reproduce to take advantage of the new food supply. In particular, they prey on the eggs and young of forest birds, which on isolated islands often have no other predators and thus have no fear of predators.[69]Some experts believe that rats are to blame for between forty percent and sixty percent of all seabird and reptile extinctions, with ninety percent of those occurring on islands. Thus man has indirectly caused the extinction of many species by accidentally introducing rats to new areas
</p>		
								
								
								
								
										
							<h4>9. Rat-free areas</h4>
                               
									<p>
				Rats are found in nearly all areas of Earth which are inhabited by human beings. The only rat-free continent is Antarctica, which is too cold for rat survival outdoors, and its lack of human habitation does not provide buildings to shelter them from the weather. However, rats have been introduced to many of the islands near Antarctica, and because of their destructive effect on native flora and fauna, efforts to eradicate them are ongoing. In particular, Bird Island (just off rat-infested South Georgia Island), where breeding seabirds could be badly affected if rats were introduced, is subject to special measures and regularly monitored for rat invasions. <br>As part of island restoration some islands' rat populations have been eradicated to protect or restore the ecology. Hawadax Island, Alaska was declared rat free after 229 years and Campbell Island, New Zealand after almost 200 years. Breaksea Island in New Zealand was declared rat free in 1988 after an eradication campaign based on a successful trial on the smaller Hawea Island nearby.<br>
In January 2015 an international "Rat Team" set sail from the Falkland Islands for the British Overseas Territory of South Georgia and the South Sandwich Islands on board a ship carrying three helicopters and 100 tons of rat poison with the objective of "reclaiming the island for its seabirds". Rats have wiped out more than 90% of the seabirds on South Georgia, and the sponsors hope that once the rats are gone, it will regain its former status as home to the greatest concentration of seabirds in the world. The South Georgia Heritage Trust, which organized the mission describes it as "five times larger than any other rodent eradication attempted worldwide".[72] That would be true if it were not for the rat control program in Alberta
The Canadian province of Alberta (population 4.25 million) is notable for being the largest inhabited area on Earth which is free of true rats due to very aggressive government rat control policies. It has large numbers of native pack rats, also called bushy-tailed wood rats, but they are forest-dwelling vegetarians which are much less destructive than true rats.<br>
Alberta was settled relatively late in North American history and only became a province in 1905. Black rats cannot survive in its climate at all, and brown rats must live near people and in their structures to survive the winters. There are numerous predators in Canada's vast natural areas which will eat non-native rats, so it took until 1950 for invading rats to make their way over land from Eastern Canada.[74]Immediately upon their arrival at the eastern border with Saskatchewan, the Alberta government implemented an extremely aggressive rat control program to stop them from advancing further. A systematic detection and eradication system was used throughout a control zone about 600 kilometres (400 mi) long and 30 kilometres (20 mi) wide along the eastern border to eliminate rat infestations before the rats could spread further into the province. Shotguns, bulldozers, high explosives, poison gas, and incendiaries were used to destroy rats. Numerous farm buildings were destroyed in the process. Initially, tons of arsenic trioxide were spread around thousands of farm yards to poison rats, but soon after the program commenced the rodenticide and medical drug warfarin was introduced, which is much safer for people and more effective at killing rats than arsenic.<br>
Forceful government control measures, strong public support and enthusiastic citizen participation continue to keep rat infestations to a minimum.[76] The effectiveness has been aided by a similar but newer program in Saskatchewan which prevents rats from even reaching the Alberta border. Alberta still employs an armed rat patrol to control rats along Alberta's borders. About ten single rats are found and killed per year, and occasionally a large localized infestation has to be dug out with heavy machinery, but the number of permanent rat infestations is zero.
	</p>		
								
	<h4>10. In culture</h4>							<br><h4>Asian cultures</h4>	
<p>	The rat (sometimes referred to as a mouse) is the first of the twelve animals of the Chinese zodiac. People born in this year are expected to possess qualities associated with rats, including creativity, intelligence, honesty, generosity, ambition, a quick temper and wastefulness. People born in a year of the rat are said to get along well with "monkeys" and "dragons", and to get along poorly with "horses".
 
Indigenous rats are allowed to run freely throughout the Karni Mata Temple.<br>
In Indian tradition, rats are seen as the vehicle of Ganesha, and a rat's statue is always found in a temple of Ganesh. In the northwestern Indian city of Deshnoke, the rats at the Karni Mata Templeare held to be destined for reincarnation as Sadhus (Hindu holy men). The attending priests feed milk and grain to the rats, of which the pilgrims also partake.
</p>							
											
					
					
					
					
					<h4>European cultures</h4>	
<p>	European associations with the rat are generally negative. For instance, "Rats!" is used as a substitute for various vulgar interjections in the English language. These associations do not draw, per se, from any biological or behavioral trait of the rat, but possibly from the association of rats (and fleas) with the 14th-century medieval plague called the Black Death. Rats are seen as vicious, unclean, parasitic animals that steal food and spread disease. However, some people in European cultures keep rats as pets and conversely find them to be tame, clean, intelligent, and playful.<br>
Rats are often used in scientific experiments; animal rights activists allege the treatment of rats in this context is cruel. The term "lab rat" is used, typically in a self-effacing manner, to describe a person whose job function requires them to spend a majority of their work time engaged in bench-level research (such as postgraduate students in the sciences).
</p>							
									
						
					<h4>Terminology</h4>	
<p>	Rats are frequently blamed for damaging food supplies and other goods, or spreading disease. Their reputation has carried into common parlance: in the English language, "rat" is often an insult or is generally used to signify an unscrupulous character; it is also used, as the term nark, to mean an individual who works as a police informant or who has turned state's evidence. Writer/director Preston Sturgescreated the humorous alias "Ratskywatsky" for a soldier who seduced, impregnated, and abandoned the heroine of his 1944 film, The Miracle of Morgan's Creek. It is a term (noun and verb) in criminal slang for an informant – "to rat on someone" is to betray them by informing the authorities of a crime or misdeed they committed. Describing a person as "rat-like" usually implies he or she is unattractive and suspicious.
Among trade unions, the word "rat" is also a term for nonunion employers or breakers of union contracts, and this is why unions use inflatable rats
</p>							
									
					
						
						<h4>11. Fiction	</h4>
                               
									<p>
				Depictions of rats in fiction are historically inaccurate and negative. The most common falsehood is the squeaking almost always heard in otherwise realistic portrayals (i.e. nonanthropomorphic). While the recordings may be of actual squeaking rats, the noise is uncommon – they may do so only if distressed, hurt, or annoyed. Normal vocalizations are very high-pitched, well outside the range of human hearing. Rats are also often cast in vicious and aggressive roles when in fact, their shyness helps keep them undiscovered for so long in an infested home.
The actual portrayals of rats vary from negative to positive with a majority in the negative and ambiguous.[81] The rat plays a villain in several mouse societies; from Brian Jacques's Redwalland Robin Jarvis's The Deptford Mice, to the roles of Disney's Professor Ratigan and Kate DiCamillo's Roscuro and Botticelli. They have often been used as a mechanism in horror; being the titular evil in stories like The Rats or H.P. Lovecraft's The Rats in the Walls[81] and in films like Willard and Ben. Another terrifying use of rats is as a method of torture, for instance in Room 101in George Orwell's Nineteen Eighty-Four or The Pit and the Pendulum by Edgar Allan Poe.
Selfish helpfulness —those willing to help for a price— has also been attributed to fictional rats.[81] Templeton, from E. B. White's Charlotte's Web, repeatedly reminds the other characters that he is only involved because it means more food for him, and the cellar-rat of John Masefield's The Midnight Folk requires bribery to be of any assistance.<br>
By contrast, the rats appearing in the Doctor Dolittle books tend to be highly positive and likeable characters, many of whom tell their remarkable life stories in the Mouse and Rat Club established by the animal-loving doctor.
Some fictional works use rats as the main characters. Notable examples include the society created by O'Brien's Mrs. Frisby and the Rats of NIMH, and others include Doctor Rat, and Rizzo the Rat from The Muppets. Pixar's 2007 animated film Ratatouille is about a rat described by Roger Ebert as "earnest... lovable, determined, [and] gifted" who lives with a Parisian garbage-boy-turned-chef.<br>
Mon oncle d'Amérique ("My American Uncle"), a 1980 French film, illustrates Henri Laborit's theories on evolutionary psychology and human behaviors by using short sequences in the storyline showing lab rat experiments.<br>
In Harry Turtledove's science fiction novel Homeward Bound, humans unintentionally introduce rats to the ecology at the home world of an alien race which previously invaded Earth and introduced some of its own fauna into its environment. A. Bertram Chandler pitted the space-bound protagonist of a long series of novels, Commodore Grimes, against giant, intelligent rats who took over several stellar systems and enslaved their human inhabitants. "The Stainless Steel Rat" is nickname of the (human) protagonist of a series of humorous science fiction novels written by Harry Harrison<br>

</p>		
								
								
								
						
					
					
			
					
					
					
					
					
