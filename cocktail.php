										<h4>
									2. Description
									</h4><br>
                               
									<p>
						The cockatiel's distinctive erectile crest expresses the animal's emotional state. The crest is dramatically vertical when the cockatiel is startled or excited, gently oblique in its neutral or relaxed state, and flattened close to the head when the animal is angry or defensive. The crest is also held flat but protrudes outward in the back when the cockatiel is trying to appear alluring or flirtatious. In contrast to most cockatoos, the cockatiel has long tail feathers roughly making up half of its total length. At 30 to 33 cm (12 to 13 in), the cockatiel is the smallest of the cockatoos which are generally larger at between 30 and 60 cm (12 and 24 in).
<br/> The "normal grey" or "wild-type" cockatiel's plumage is primarily grey with prominent white flashes on the outer edges of each wing. The face of the male is yellow or white, while the face of the female is primarily grey or light grey, and both sexes feature a round orange area on both ears, often referred to as "cheddar cheeks". This orange colouration is generally vibrant in adult males, and often quite muted in females. Visual sexing is often possible with this variant of the bird.
Cockatiels are relatively vocal birds, the calls of the male being more varied than that of the female. Cockatiels can be taught to sing specific melodies and speak many words and phrases.

	</p>	
										
										
										
										<h4>
								3. Sexual dimorphism
									</h4><br>
                               
									<p>
						All wild cockatiel chicks and juveniles look female, and are virtually indistinguishable from the time of hatching until their first moulting. They display horizontal yellow stripes or bars on the ventral surface of their tail feathers, yellow spots on the ventral surface of the primary flight feathers of their wings, a grey coloured crest and face, and a dull orange patch on each of their cheeks.
Adult cockatiels are sexually dimorphic, though to a lesser degree than many other avian species. This is only evident after the first moulting, typically occurring about six to nine months after hatching: the male loses the white or yellow barring and spots on the underside of his tail feathers and wings. The grey feathers on his cheeks and crest are replaced by bright yellow feathers, while the orange cheek patch becomes brighter and more distinct. The face and crest of the female will typically remain mostly grey, though also with an orange cheek patch. Additionally, the female commonly retains the horizontal barring on the underside of her tail feathers.
The colour in cockatiels is derived from two pigments: melanin (which provides the grey colour in the feathers, eyes, beak, and feet), and lipochromes (which provide the yellow colour on the face and tail and the orange colour of the cheek patch). The grey colour of the melanin overrides the yellow and orange of the lipochromes when both are present.
The melanin content decreases in the face of the males as they mature, allowing the yellow and orange lipochromes to be more visible, while an increase in melanin content in the tail causes the disappearance of the horizontal yellow tail bars.
In addition to these visible characteristics, the vocalisation of adult males is typically louder and more complex than that of females.
    </p>	
										
											
										
										<h4>
								4. Colour mutations
								</h4><br>
                               
									<p>
						Worldwide there are currently 22 cockatiel colour mutations established in aviculture with eight being exclusive to Australia. Mutations in captivity have emerged in various colours, some quite different from those observed in nature. Wild cockatiels are grey with visible differences between males and females. Male grey cockatiels typically have yellow heads while the female has a grey head. Juveniles tend to look like females with pinker beaks. The pied mutation first appeared in California in 1949. This mutation is a blotch of colour on an otherwise solid-coloured bird. For example, this may appear as a grey blotch on a yellow cockatiel.
Lutino coloration was first seen in 1958. These birds lack the grey of their wild counterparts and are white to soft yellow. This is a popular colour; due to inbreeding, these cockatiels often have a small bald patch behind their crests. The cinnamon mutation, first seen in the 1950s, is very similar in appearance to the grey; however, these birds have a warmer, browner colouring. Pearling was first seen in 1967. This is seen as a feather of one colour with a different coloured edge, such as grey feathers with yellow tips. This distinctive pattern is on a bird's wings or back. The albino colour mutation is a lack of pigment. These birds are white with red eyes. Fallow cockatiels first appeared sometime in the 1970s. This mutation shows as a bird with cinnamon colouring with yellow sections. Other mutations include: emerald/olive, dominant and recessive silver as well as mutations exclusive to Australia. These are: Australian fallow, faded (west coast silver), dilute/pastel silver (east coast silver) silver spangle (edged dilute), platinum, suffused (Australian olive) and pewter. Other mutations such as face altering mutations include; whiteface, pastelface, dominant yellow cheek, sex-linked yellow cheek, gold cheek, creamface and the Australian yellow cheek.
Cockatiel colour mutations can become even more complex as one bird can have multiple colour mutations. For example, a yellow lutino cockatiel may have pearling – white spots on its back and wings. This is a double mutation. An example of a quadruple mutation would be cinnamon cockatiel with yellowface colouring with pearling and pied markings.[6]
</p>	
										
										
								
										<h4>
								5. Distribution and habitat
									</h4><br>
								
                               
									<p>
				Cockatiels are native to Australia, where they are found largely in arid or semi-arid country, but always close to water. Largely nomadic, the species will move to where food and water is available.[2] They are typically seen in pairs or small flocks.[2] Sometimes, hundreds will flock around a single body of water. To many farmers' dismay, they often eat cultivated crops. They are absent from the most fertile south west and south east corners of the country, the deepest Western Australian deserts, and Cape York Peninsula. They are the only cockatoo species which can sometimes reproduce in the end of their first year.</p>				
								
					<h4>6. Life span</h4>
                               
									<p>
			The cockatiel's lifespan in captivity is generally given as 16 to 25 years,[7] though it is sometimes given as short as 10 to 15 years, and there are reports of cockatiels living as long as 32 years, the oldest confirmed specimen reported being 36 years old.[8] Diet and exercise are major determining factors.			
					
