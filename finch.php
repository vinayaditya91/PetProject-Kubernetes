										The taxonomy of the finch family, in particular the cardueline finches, has a long and complicated history. The study of the relationship between the taxa has been confounded by the recurrence of similar morphologies due to the convergence of species occupying similar niches.[3] In 1968 the American ornithologist Raymond Andrew Paynter Jr. wrote:
Limits of the genera and relationships among the species are less understood – and subject to more controversy – in the carduelines than in any other species of passerines, with the possible exception of the estrildines [waxbills].[4]
Beginning in around 1990 a series of phylogenetic studies based on mitochondrial and nuclear DNA sequences resulted in substantial revisions being made to the taxonomy. Several groups of birds that had previously been assigned to other families were found to be related to the finches. The Neotropical Euphonia and the Chlorophonia were formerly placed in the tanager family Thraupidae due to their similar appearance but analysis of mitochondrial DNA sequences revealed that both genera were more closely related to the finches. They are now placed in a separate subfamily Euphoniinae within the Fringillidae.[5][6] The Hawaiian honeycreepers were at one time placed in their own family, Drepanididae but were found to be closely related to the Carpodacus rosefinches and are now placed within the Carduelinae subfamily.[3] The three largest genera, Carpodacus, Carduelis and Serinus were found to be polyphyletic.[3][7][8] Each was split into monophyletic genera. The American rosefinches were moved from Carpodacus to Haemorhous. Carduelis was split by moving the greenfinches to Chloris and a large clade into Spinus leaving just three species in the original genus. Thirty seven species were moved from Serinus to Crithagra leaving eight species in the original genus.[6] Today the family Fringillidae is divided into three subfamilies, the Fringillinae containing a single genus with the chaffinches, the Carduelinae containing 183 species divided into 49 genera, and the Euphoniinae containing the Euphonia and the Chlorophonia
