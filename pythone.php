										<h4>
									1. Distribution and habitat
									</h4><br>
                                <img class="img-fluid" src="media/image55.jpeg" alt="" style="height:400px;width:800px;"><br><br>
									<p>
									They are found in west Sub Saharan Africa from Senegal, Mali, Guinea-Bissau, Guinea, Sierra Leone, Liberia, Ivory Coast, Ghana, Benin, and Nigeria through Cameroon, Chad, and the Central African Republic to Sudan and Uganda. No type locality was given in the original description.<br>
The ball python bears a strong physical resemblance to the Burmese python, whose adaptive abilities have caused it to become classified as an invasive species in places such as the Florida Everglades. The ball python, however, has not been known to reproduce in the wild outside of its native range and no reproducing wild populations are known in Florida.<br>
Ball pythons prefer grasslands, savannas, and sparsely wooded areas,[2] but have been shown to adapt to all types of environments. Males tend to display more semi-arboreal behaviours, whilst females tend towards terrestrial behaviours
</p>
										
										
										
										<h4>
									2. Behavior
									</h4><br>
                               
									<p>
							This terrestrial species is known for its defense strategy that involves coiling into a tight ball when threatened, with its head and neck tucked away in the middle. In this state, it can literally be rolled around. Favored retreats include mammal burrows and other underground hiding places, where they also aestivate. In captivity, they are considered good pets, with their relatively small size and placid nature making them easy to handle.
							</p>	
										
										
										
										<h4>
								3.Diet
									</h4><br>
                               
									<p>
In the wild, their diet consists mostly of small mammals, such as African soft-furred rats, shrews, and striped mice and birds. Younger pythons, under 70 cm total length, and males prey almost exclusively on small birds (nestlings and immature young) whilst pythons greater than 70 cm total length, and females prey almost exclusively on small mammals

</p>	
										
											
										
										<h4>
								4. Reproduction
								</h4><br>
                               
									<p>
						Females are oviparous, with 3 to 11 rather large, leathery eggs being laid (four to six are most common).[5] These are incubated by the female under the ground (via a shivering motion), and hatch after 55 to 60 days. Sexual maturity is reached at 11–18 months for males, and 20–36 months for females. Age is only one factor in determining sexual maturity and ability to breed; weight is the second factor. Males breed at 600 g or more, but in captivity are often not bred until they are 800 g (1.7 lb), although in captivity, some males have been known to begin breeding at 300–400 g. Females breed in the wild at weights as low as 800 g though 1200 g or more in weight is most common; in captivity, breeders generally wait until they are no less than 1500 g (3.3 lb). Parental care of the eggs ends once they hatch, and the female leaves the offspring to fend for themselves.
						

</p>	
										
										
								
										<h4>
								5. Captivity
									</h4><br>
								
                               
									<p>
					Wild-caught specimens have greater difficulty adapting to a captive environment, which can result in refusal to feed, and they generally carry internal or external parasites. Specimens have survived for over 40 years in captivity, with the oldest recorded ball python being kept in captivity 47 years and 6 months until its death in 1992 at the Philadelphia Zoo.<br>
Hundreds of different color patterns are available in captive snakes. Some of the most common are spider, pastel, albino, Mojave, and lesser. Breeders are continuously creating new designer morphs, and over 5,300 different morphs currently exist.
</p>			
					<h4>6. Breeding</h4>
                               
									<p>
				Ball Pythons are one of the most common reptiles bred in captivity. They usually are able to produce a clutch of six eggs on average, but clutch sizes also range from one to eleven. Ball pythons reach sexual maturity at the age of two to two and a half years and a weight of 1500 grams. These snakes usually lay one clutch per year and the eggs hatch around sixty days later. Usually these eggs are artificially incubated in a captive environment at temperatures between 88-90 degrees Fahrenheit.Some captive breeders use ultra-sounding technology to verify the progress of reproductive development. This can help to increase chances of successful fertilization as the ultra-sound can help predict best times to introduce males and females during breeding season.[15] It has been shown that the Spider gene is connected with major neurological issues, specifically related to the snake's sense of balance. </p>					
								
							<h4>7. Beliefs and folklore</h4>
                               
									<p>
				This species is particularly revered in the traditional religion of the Igbo people of southeastern Nigeria. It is considered symbolic of the earth, being an animal that travels so close to the ground. Even among many Christian Igbos, these pythons are treated with great care whenever they happen to wander into a village or onto someone's property; they are allowed to roam freely or are very gently picked up and placed out in a forest or field away from any homes. If one is accidentally killed, many communities on Igbo land still build a coffin for the snake's remains and give it a short funeral<br></p>							
								
								
								
					
					
					
			
					
					
					
					
					
