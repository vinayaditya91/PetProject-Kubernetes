									<blockquote class="generic-blockquote" style="text-align: justify;">
						Python, from the Greek word (πύθων/πύθωνας), is a genus of nonvenomous Pythonidae[2] found in Africa and Asia. Until recently, seven extant species were recognised;[2] however, three subspecies have been promoted and a new species recognised. A member of this genus, P. reticulatus, is among the longest snake species and extant reptiles in the world.<br>In Africa, members of the genus are found in the tropics south of the Sahara, but not in the extreme south-western tip of southern Africa (Western Cape) or in Madagascar. In Asia, they are found from Bangladesh, Nepal, India, Pakistan, and Sri Lanka, including the Nicobar Islands, through Myanmar, east to Indochina, southern China, Hong Kong and Hainan, as well as in the Malayan region of Indonesia and the Philippines <br>
						The ball python (Python regius), also known as the royal python,[2] is a python species found in sub-Saharan Africa. Like all other pythons, it is a nonvenomous constrictor. This is the smallest of the African pythons and is popular in the pet trade, largely due to its small size and typically docile temperament. No subspecies are currently recognized.[3] The name "ball python" refers to the animal's tendency to curl into a ball when stressed or frightened.[4] The name "royal python" (from the Latin regius) comes from the fact that rulers in Africa would wear the python as jewelry.<br>
 

Maximum adult length of this species is 182 cm (6.0 ft).[5]Females tend to be slightly bigger than males, maturing around 122–137 cm (4.0–4.5 ft). Males typically grow to around 90–107 cm (3.0–3.5 ft).[6] Their build is stocky,[2]while the head is relatively small. The scales are smooth[5] and both sexes have anal spurs on either side of the vent.[7] Although males tend to have larger spurs, this is not definitive, and sex is best determined by manual eversion of the male hemipenes or inserting a probe into the cloaca to check the presence of an inverted hemipenis (if male).[8] When probing to determine sex, males typically measure eight to 10 subcaudal scales, and females typically measure two to four subcaudal scales.[5]
The color pattern is typically black or dark brown with light brown or gold sides and dorsal blotches. The belly is a white or cream that may include scattered black markings.[5] However, those in the pet industries have, through selective breeding, developed many morphs (genetic mutations) with altered colors and patterns.

