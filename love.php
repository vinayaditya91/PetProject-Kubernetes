									<blockquote class="generic-blockquote">
						A lovebird is the common name of Agapornis (Greek: αγάπη agape 'love'; όρνις ornis 'bird'), a small genus of parrot. Eight species are native to the African continent, with the grey-headed lovebird being native to Madagascar.<br>
Social and affectionate, the name comes from the parrots' strong, monogamous pair bonding and the long periods which paired birds spend sitting together. Lovebirds live in small flocks and eat fruit, vegetables, grasses, and seeds. Black-winged lovebirds also eat insects and figs, and the black-collared lovebirds have a special dietary requirements for native figs, making them problematic to keep in captivity.<br>
Some species are kept as pets, and several colored mutations have been selectively bred in aviculture. The average lifespan is 10 to 15 years
 </blockquote><br>
										<h4>
									1. Description
									</h4><br>
                                <img class="img-fluid" src="https://073bddbe7aa062defd37fde3-cwzdvdpfea.netdna-ssl.com/wp-content/uploads/2013/05/Love-Birds-Parrots.jpeg" alt="" style="height:400px;width:800px;"><br><br>
									<p>
									Lovebirds are 13 to 17 cm (5 to 7 in) in length, up to 24 cm in wingspan with 9 cm for a single wing and 40 to 60 g (1 1⁄2 to 2 oz) in weight. They are among the smallest parrots, characterized by a stocky build, a short blunt tail, and a relatively large, sharp beak. Wildtype lovebirds are mostly green with a variety of colors on their upper body, depending on the species. The Fischer's lovebird, black-cheeked lovebird, and the masked lovebird have a prominent white ring around their eyes. Many color mutant varieties have been produced by selective breeding of the species that are popular in aviculture
</p>
										
										
										
										<h4>
									2. Taxonomy
									</h4><br>
                               
									<p>
						The lovebird genus comprises nine species of which five are monotypic and four are divided into subspecies.[3] Eight of them are native in the mainland of Africa and the Madagascar lovebird is native to Madagascar. In the wild, the different species are separated geographically.
Traditionally, lovebirds are divided into three groups:<br>
•	the sexually dimorphic species: Madagascar, Abyssinian, and red-headed lovebird<br>
•	the intermediate species: peach-faced lovebird<br>
•	the white-eye-ringed species: masked, Fischer's, Lilian's, and black-cheeked lovebirds<br>
However, this division is not fully supported by phylogenetic studies, as the species of the dimorphic group are not grouped together in a single clade.
Species and subspecies:<br>
•	Rosy-faced lovebird, Agapornis roseicollis, (Vieillot, 1818)—or peach-faced lovebird<br>
•	Agapornis roseicollis catumbella, B.P. Hall, 1952<br>
•	Agapornis roseicollis roseicollis, (Vieillot 1818)<br>
•	Yellow-collared lovebird, Agapornis personatus, Reichenow, 1887—or masked lovebird<br>
•	Fischer's lovebird, Agapornis fischeri, Reichenow, 1887<br>
•	Lilian's lovebird, Agapornis lilianae, Shelley, 1894—or Nyasa lovebird<br>
•	Black-cheeked lovebird, Agapornis nigrigenis, W.L. Sclater, 1906<br>
•	Grey-headed lovebird, Agapornis canus, (Gmelin, 1788)—or Madagascar lovebird<br>
•	Agapornis canus ablectaneus, Bangs, 1918<br>
•	Agapornis canus canus, (Gmelin, 1788)<br>
•	Black-winged lovebird, Agapornis taranta, (Stanley, 1814)—or Abyssinian lovebird<br>
•	Red-headed lovebird, Agapornis pullarius, (Linnaeus, 1758)—or red-faced lovebird<br>
