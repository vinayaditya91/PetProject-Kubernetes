										<h4>
								3. Nesting
									</h4><br>
                               
									<p>
							Depending on the species of lovebird, the female will carry nesting material into the nest in various ways. The peach-faced lovebird tucks nesting material in the feathers of its rump,[5] while the masked lovebird carries nesting material back in its beak. Once the lovebirds start constructing their nest, mating will follow. During this time, the lovebirds will mate repeatedly. Eggs follow 3–5 days later. The female will spend hours inside her nesting box before eggs are laid. Once the first egg is laid, a new egg will follow every other day until the clutch is complete, typically at four to six eggs. Even without a nest or a male, lovebirds sometimes produce eggs
</p>	
										
											
										
										<h4>
								4. Feral populations
								</h4><br>
                               
									<p>
							Feral populations of Fischer's lovebirds and masked lovebirds live in cities of East Africa. There are interspecific hybrids that exist between these two species. The hybrid has reddish-brown on head and has orange on upper chest, but otherwise resembles the masked lovebird.<br>
Feral lovebirds are also present in warmer cities of the United States. These include Phoenix, Arizona, and Austin, Texas. Several species are also found in feral populations in San Diego, California.<br>
In Victoria Australia around Melbourne a version locally called an African Lovebird has an aggressive breeding nature. In an aviary with smaller birds it is common to find other smaller species decapitated overnight. The surviving partner also succumbs within 3 weeks.<br>
There are two feral colonies present in the Pretoria region (Silver Lakes and Centurion) in South Africa. They probably originated from birds that escaped from aviaries. They consist mostly of masked, black cheeked, Fischer and hybrid birds and vary in colors. White (not albino) and yellow as well as blue occur in many cases. The white ringed eyes are very prominent
</p>	
										
										
								
										<h4>
								5. Aviculture
									</h4><br>
								
                               
									<p>
					With their inclination to bond, lovebirds can form long-term relationships with people as well as other lovebirds. Aggression is easily aroused in lovebirds, however, and they may bite unless humans establish a bond with gentle handling. Provided with adequate space, a stimulating environment, and appropriate nutrition, lovebirds can become cherished companion parrots. They love to snuggle and will often preen their favorite people.<br>
It is preferable to obtain birds bred in captivity, rather than birds caught in the wild. Wild birds may harbor diseases such as avian polyomavirus.[7] Captured wild lovebirds also may mourn the loss of association with a mate or a flock. Their age is likely to be unknown, and they may have an unsuitable personality for domestication. Currently, lovebirds are no longer imported from the wild in the United States. Contrary to what their name might suggest, lovebirds are not necessarily best kept in pairs; paired lovebirds are less likely to have intense relationships with humans. Birds socialized from a very early age, while being brought up by parents, make very good pets. The practice of hand-feeding young psittacines, including lovebirds, outside of a medical emergency has been outlawed in the Netherlands since 1 July 2014[8] and lovebird chicks should stay with their parents until they can eat independently, at minimum 55 days after hatching. However, single birds require frequent attention to stay happy, and if the owner has limited time to spend daily with a single lovebird, it is preferable to give the lovebird a companion of the same species, or a companion of another parrot species known to get along well with lovebirds. It is important to use cages suitable for smaller birds, as wide-spaced bars can cause damage to these small hookbills. Lovebirds can become very interactive with humans, and when comfortable, will willingly perch on a finger or shoulder.<br>
Some lovebirds talk, but many will not: there is a chance they may learn to mimic human sounds if taught to do so at a young age. Lovebirds are noisy, with calls ranging from cheerily pleasant to highly irritating; in the wild, parrots must call to each other over long distances to keep flocks together, and it is through such signals that they make most of their communication. It is best to spend frequent, short periods of time with a lovebird, rather than having just one or two interactions every day.
</p>				
								
					<h4>6. Sexual characteristics and behavior</h4>
                               
									<p>
			Determining the sex of a lovebird is difficult. At maturity of one year, it may show signs of whether it is male or female, such as ripping up paper and stuffing it into its feathers (female behavior) or regurgitating for its owners (male behavior: the male feeds the nesting female). This behavior is not a reliable indicator, however. The only sure method is DNA testing; however, some experts can sex lovebirds by feeling beneath the body. There are two sharp points beneath the cloaca known as the pelvic bone. If the points are nearer together, the bird is male; if the points are further away, it is female (females must have a larger pelvis to lay eggs).</p>					
								
							<h4>7. Housing and environment[</h4>
                               
									<p>
					Lovebirds require an appropriately sized cage or aviary. Minimum recommended space per bird is 1m×1m×1m. Lovebird's beaks are made of keratin, which grows continuously. Chewing and destroying wood toys and perches helps to keep beaks trim. Cuttlebones help provide beak-trimming and a source of calcium and other necessary minerals. Natural perches and special rough surfaced perches of varying diameters placed at different levels in the cage will allow greater climbing mobility and gives them a choice to select the most comfortable spot to roost.[11]They also require plenty of toys, such as willow branches, swings, tunnels, boxes and safe things to chew on and play with.<br>
Lack of toys, keeping the birdcage covered too many hours, and lack of companionship or social stimulation may lead to boredom, stress and psychological or behavioral problems (nervousness, aggression, feather-plucking, screaming, depression, immunosuppression). Lovebirds are social birds and will enjoy several hours of interaction a day. Without this interaction, daily exercise, a roomy cage/aviary, and many toys to play with, they may resort to feather-plucking or screaming, and both behaviors can be difficult to cure. If the owner leaves the house, he or she should leave a radio or TV set playing to provide sound for the bird.[12] Lovebirds are intelligent, and if a relationship is to form they need a human who will dedicate lots of time to them. Lovebirds enjoy baths and like to sun themselves daily.
<br></p>							
							<h4>8. Grooming</h4>
                               
									<p>
					As with pet parrots in general, the tips of lovebirds' toenails should wear down adequately by the parrot climbing over rough surfaced perches. If the parrot has an inactive lifestyle the toenails may grow long and need to be trimmed.[13] Only the very tips of the toenails are trimmed. If too much of a toenail is trimmed away, it will be painful and bleed from the blood vessels in the center of the nail.[13][14] This bleeding should be stopped as soon as possible with the use of styptic gel or powder. Sharp pointed toenails can be blunted by simply filing the point. These procedures are usually done with the help of an assistant carefully holding the parrot wrapped in a towel</p>		
								
								
								
								
										
							<h4>9. Diet in captivity</h4>
                               
									<p>
			A fresh mix (with or without dehydrated fruits and/or vegetables) of excellent quality combining various seeds, grains and nuts generally represent the typical basic diet. Ideally the basic mix will contain or will be supplemented with an about 30% portion of any bio/organic (naturally coloured and flavoured and without any conservative agent) and/or of any natural (naturally coloured, flavoured and preserved) pellets.
			<br><span style:"font-weight:bold;">Ideal basic dry mix:</span>
			
			- 40% Yellow millet - 18% Canary seed - 10% White millet - 9% Peeled oats - 5% Japanese millet - 4% Safflower - 4% Buckwheat - 3% Paddy rice - 3% Oats - 2% Linseed - 2% Hempseed
Or else a blend of commercial mixes:<br/>
1/2 typical basic dry mix for cockatiel parakeets and/or small parrot/parakeet species (i.e. kākāriki parakeets, monk parakeet, Australian grass parakeets, magnifiscent parakeets, Pyrrhura conures, small Afro-Asian ringnecked parakeets).<br>
1/2 typical basic dry mix for budgerigar parakeets and other miniature parrot/parakeet species (i.e. parrotlets, lineolated parrot, Bourke's parakeet, grass parakeets).<br>
<br><span style:"font-weight:bold;">Cooked and fresh foods:</span><br>
			
			These foods and goods must be offered as often as possible (particularly cereals, fruits, greens/weeds, legumes/pulses and vegetables), ideally on a daily basis or at least weekly. The pellet ratio should be increased to 1/2 of the basic dry mix when cooked and/or fresh foods are occasionally or rarely offered.
		<br><span style:"font-weight:bold;">Whole grains and cereals:</span><br>
		
		Amaranth, barley, couscous, flax, oat, rice varieties such as basmati, brown rice, jasmine rice, quinoa, wheat, lightly toasted whole-grain Waffles, non-toasted whole grain breads (i.e. corn-bread, multi-cereal, 14 cereals, whole-wheat), al dente cooked pastas.
		<br><span style:"font-weight:bold;">Edible blossoms and flowers:</span><br>
		carnations, chamomille, chives, dandelion, day lilies, eucalyptus, fruit trees' blossoms, herbs' blossoms, hibiscus, honeysuckle, impatiens, lilac, nasturiums, pansies, passion flower (passifloræ), rosees, sunflowers, tulips, violets.
	
	
		<br><span style:"font-weight:bold;">Soaked and/or sprouted cereals and grains:</span><br>
	
	Aduki beans, alfalfa beans, buckwheat, lentils, millets & sprays, mung beans, pinto beans, red kidney beans, sesame seeds, sunflower seeds.<br>
Sprouts should be offered at least weekly) to juveniles, adults, pets & breeder specimens in the breeding season. And on a daily or at least weekly basis to breeders in breeding-season rearing their chicks.<br>
•	Caution with only lima and navy beans which are toxic when sprouted but healthy when well cooked.
Fresh water and a mineral block must be available at all times.<br>
Adding these foods provides additional nutrients and can prevent obesity and lipomas, as can substituting millet, which is relatively low in fat, for higher-fat seed mixes. Adult lovebirds often do not always adapt readily to dietary additions, so care must be taken to introduce healthy diets as young as possible (ideally weaned onto fresh foods before introducing chicks onto seeds). Lovebirds like any other parrots learn mainly by mimicry and thus most adult lovebirds will be easily encouraged to try new foods by observing another bird eating the food, or by placing the new food on a mirror.
Parrot species (including cockatiels) are biologically vegetarian species.[citation needed] Consequently, they should be fed vegetarian diets that are ideally supplemented with vegetable proteins provided by the combination of any type of wholegrain/cereal with any type of legume/pulse. Eggs (hard-boiled and/or scrambled) with crushed shells are the only appropriately healthy source of animal proteins.[citation needed] Mostly for birds in either breeding, growing, moulting and/or recovering conditions. High levels of proteins (most particularly animal proteins) is unhealthy for lovebirds living under any alternate conditions (i.e. non-breeding, pets).

	
	
	
	
	
	
	
	
		</p>		
								
	<h4>10. Aggression problems with other birds and animal species</h4>							<br>	
<p>	Because of their dominant and territorial nature, lovebirds should be supervised when socializing with other species and genera (whether it be cat, dog, small mammal or other bird species). Lovebirds can be aggressive to other birds, even to other lovebirds. Hand-raised lovebirds tend not to be scared and pose even more of a threat to themselves. Toe biting can occur when lovebirds are socializing/housed with small birds (i.e. parrotlets, budgies, and even docile cockatiels). They should not be housed with other bird genera as they can be injured or pose a threat to other birds.
 
</p>							
											
					
					
					
					
					<h4>11. Pets and bird safety</h4>
<p>	Lovebirds are very vocal birds, making loud, high-pitched noises. Some make noise all day, especially during dawn and dusk. This is a normal parrot behavior as flock animals, where they are calling to each other before the start of the day and just before they settle down for the night.<br>
Lovebirds are also very active and love to chew things. When they are flying within a household, it is wise to watch them carefully and protect any furniture, electrical wiring or anything else that they could possibly chew on. Try to place fresh willow or oak branches in a favorite spot (e.g. near a high/sunny platform) to satisfy this natural behavior. Other things to be aware of when keeping lovebirds (also applies to other pet birds) at home are the danger spots in the house, such as open toilet bowls for drowning, clear glass walls which might be rammed in full force by the bird, fumes from all Teflon or anti-stick coating in the kitchen, microwave coatings, oven coatings and chemical fumes from regular household cleaning products. Interaction with other pets in the house must also be supervised, such as cats and dogs which may view the lovebird as a prey animal.<br>
Some people who keep birds as pets practice the clipping of the flight feathers for safety reasons as mentioned above. This also promotes tameness between the bird and the owner; the bird is unable to react to flee and must become dependent on its owner for lengthy travel.<br>
Lovebirds of different species can mate and produce both sterile and fertile hybrid offspring, for example Agapornis personatus mate with Agapornis fischeri will produce fertile hybrid offspring. These offspring have behaviors of both parents. It is recommended to only place birds of the same species together, or of the same sex for this reason.
There are two lovebird societies in the United States: the Agapornis Breeders & Exhibitors and the African Love Bird Society.
</p>							
									
				
								
								
						
					
					
			
					
					
					
					
					
