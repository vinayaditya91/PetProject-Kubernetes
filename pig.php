										<h4>
									1. Name
									</h4><br>
                                <img class="img-fluid" src="https://wallpaperplay.com/walls/full/2/6/c/132901.jpg" alt="" style="height:400px;width:800px;"><br><br>
									<p>
										The scientific name of the common species is Cavia porcellus, with porcellus being Latin for "little pig". Cavia is New Latin; it is derived from cabiai, the animal's name in the language of the Galibi tribes once native to French Guiana.[5] Cabiai may be an adaptation of the Portuguese çavia (now savia), which is itself derived from the Tupi word saujá, meaning rat.[6] Guinea pigs are called quwi or jaca in Quechua and cuy or cuyo (plural cuyes, cuyos) in the Spanish of Ecuador, Peru, and Bolivia.[7] Ironically, breeders tend to use the more formal "cavy" to describe the animal, while in scientific and laboratory contexts, it is far more commonly referred to by the more colloquial "guinea pig".[8]
How the animals came to be called "pigs" is not clear. They are built somewhat like pigs, with large heads relative to their bodies, stout necks, and rounded rumps with no tail of any consequence; some of the sounds they emit are very similar to those made by pigs, and they also spend a large amount of time eating.[8][9] They can survive for long periods in small quarters, like a 'pig pen', and were thus easily transported on ships to Europe.[8]
</p>
										
										
										
										<h4>
									2. History
									</h4><br>
                               
									<p>
								The guinea pig was first domesticated as early as 5000 BC for food by tribes in the Andean region of South America (the present-day southern part of Colombia, Ecuador, Peru, and Bolivia),[16] some thousands of years after the domestication of the South American camelids.[17] Statues dating from circa 500 BC to 500 AD that depict guinea pigs have been unearthed in archaeological digs in Peru and Ecuador.[18] The Moche people of ancient Peru worshipped animals and often depicted the guinea pig in their art.[19] From about 1200 AD to the Spanish conquest in 1532, selective breeding resulted in many varieties of domestic guinea pigs, which form the basis for some of the modern domestic breeds.[20] They continue to be a food source in the region; many households in the Andean highlands raise the animal, which subsists on the family's vegetable scraps.[21] Folklore traditions involving guinea pigs are numerous; they are exchanged as gifts, used in customary social and religious ceremonies, and frequently referenced in spoken metaphors.[22] They also play a role in traditional healing rituals by folk doctors, or curanderos, who use the animals to diagnose diseases such as jaundice, rheumatism, arthritis, and typhus.[23] They are rubbed against the bodies of the sick, and are seen as a supernatural medium.[24] Black guinea pigs are considered especially useful for diagnoses.[25] The animal also may be cut open and its entrails examined to determine whether the cure was effective.[26] These methods are widely accepted in many parts of the Andes, where Western medicine is either unavailable or distrusted.
								<br>Spanish, Dutch, and English traders brought guinea pigs to Europe, where they quickly became popular as exotic pets among the upper classes and royalty, including Queen Elizabeth I.[16] The earliest known written account of the guinea pig dates from 1547, in a description of the animal from Santo Domingo; because cavies are not native to Hispaniola, the animal was earlier believed to have been introduced there by Spanish travelers.[1] However, based on more recent excavations on West Indian islands, the animal must have been introduced by ceramic-making horticulturalists from South America to the Caribbean around 500 BC,[28] and it was present in the Ostionoid period, for example, on Puerto Rico,[29] long before the advent of the Spaniards. The guinea pig was first described in the West in 1554 by the Swiss naturalist Conrad Gessner.[30] Its binomial scientific name was first used by Erxleben in 1777; it is an amalgam of Pallas' genericdesignation (1766) and Linnaeus' specific conferral (1758).[1] The earliest known European illustration of a domestic guinea pig is a painting (artist unknown) in the collection of the National Portrait Gallery in London, dated to 1580, which shows a girl in typical Elizabethan dress holding a tortoise-shell guinea pig in her hands; she is flanked by her two brothers, one of whom holds a pet bird.[31]The picture dates from the same period as the oldest recorded guinea pig remains in England, which are a partial cavy skeleton found at Hill Hall, an Elizabethan manor house in Essex, and dated to around 1575.[31]
								</p>	
										
										
										
										<h4>
								3. Characteristics
									</h4><br>
                               
									<p>
								Guinea pigs are large for rodents; the common pet breeds weigh between 700 and 1,200 g (1.5 and 2.6 lb) when full grown and measure between 20 and 25 cm (8 and 10 in) in length.[32] Some livestock breeds weigh 3 kilograms (6.6 lb) when full grown.[33] Pet breeds live an average of four to five years, but may live as long as eight.[34] According to the 2006 Guinness World Records, the longest living guinea pig survived 14 years, 10.5 months.[35] Most guinea pigs have fur, but one laboratory breed adopted by some pet owners, the skinny pig, is a mostly furless breed. Some breeds are longfur breeds such as the Peruvian, the Silkie, and the Texel.
In the 1990s, a minority scientific opinion emerged proposing that caviomorphs, such as guinea pigs, chinchillas, and degus, are not rodents and should be reclassified as a separate order of mammals (similar to lagomorphs).[36][37] Subsequent research using wider sampling restored the consensus among mammalian biologists regarding the current classification of rodents as monophyletic

</p>	
										
											
										
										<h4>
								3. Behavior
								</h4><br>
                               
									<p>
								Guinea pigs can learn complex paths to food, and can accurately remember a learned path for months. Their strongest problem-solving strategy is motion.[40] While guinea pigs can jump small obstacles, most of them are poor climbers, and are not particularly agile. They startle extremely easily, and either freeze in place for long periods or run for cover with rapid, darting motions when they sense danger.[41]Larger groups of startled guinea pigs "stampede", running in haphazard directions as a means of confusing predators.[42] When happily excited, guinea pigs may repeatedly perform little hops in the air (known as "popcorning"), a movement analogous to the ferret's war dance.[43] They are also good swimmers<br>
								    Guinea pig eyesight is not as good as that of a human in terms of distance and color, but they have a wider angle of vision (about 340°) and see in partial color (dichromacy). They have well-developed senses of hearing, smell, and touch.[48][49]
Vocalization is the primary means of communication between members of the species.[50] These are the most common sounds made by the guinea pig
<br>•	A "wheek" is a loud noise, the name of which is onomatopoeic, also known as a whistle. An expression of general excitement, it may occur in response to the presence of its owner or to feeding. It is sometimes used to find other guinea pigs if they are running. If a guinea pig is lost, it may wheek for assistance.  <br>
•	A rumbling sound is normally related to dominance within a group, though it can also come as a response to being scared or angry. In the case of being scared, the rumble often sounds higher and the body vibrates shortly. While courting, a male usually purrs deeply, swaying and circling the female[52] in a behavior called rumblestrutting. A low rumble while walking away reluctantly shows passiveresistance.  <br>
•	Chutting and whining are sounds made in pursuit situations, by the pursuer and pursuee, respectively.  <br>
•	A chattering sound is made by rapidly gnashing the teeth, and is generally a sign of warning. Guinea pigs tend to raise their heads when making this sound.<br>
•	Squealing or shrieking is a high-pitched sound of discontent, in response to pain or danger.  <br>
•	Chirping, a less common sound, likened to bird song, seems to be related to stress or discomfort, or when a baby guinea pig wants to be fed. Very rarely, the chirping will last for several minutes. 
</p>	
										
										
								
										<h4>
								4. Environment
									</h4><br>
									<h4>Natural habitat</h4>
                               
									<p>
						C. porcellus is not found naturally in the wild; it is likely descended from closely related species of cavies, such as C. aperea, C. fulgida, and C. tschudii, which are still commonly found in various regions of South America.[1] Studies from 2007–2010 applied molecular markers[53][54] and studied the skull and skeletal morphology of current and mummified animals,[55] thereby revealing the ancestor to most likely be Cavia tschudii. Some species of cavy identified in the 20th century, such as C. anolaimae and C. guianae, may be domestic guinea pigs that have become feral by reintroduction into the wild.[20] Wild cavies are found on grassy plains and occupy an ecological niche similar to that of cattle. They are social, living in the wild in small groups that consist of several females (sows), a male (boar), and the young (which, in a break with the preceding porcine nomenclature, are called "pups" not "piglets"). They move together in groups (herds) eating grass or other vegetation, and do not store food.[56] While they do not burrow themselves or build nests, they frequently seek shelter in the burrows of other animals, as well as in crevices and tunnels formed by vegetation.[56] They tend to be most active during dawn and dusk (crepuscular), when it is harder for predators to spot them.[41]
						</p>	
								
								
								
					<h4>Domestic habitat</h4>
                               
									<p>
						Domesticated guinea pigs thrive in groups of two or more; groups of sows, or groups of one or more sows and a neutered boar are common combinations, but boars can sometimes live together. Guinea pigs learn to recognize and bond with other individual pigs, and testing of boars shows their neuroendocrine stress response is significantly lowered in the presence of a bonded female when compared to the presence of unfamiliar females.[57] Groups of boars may also get along, provided their cage has enough space, they are introduced at an early age, and no females are present.[58] Domestic guinea pigs have developed a different biological rhythm from their wild counterparts, and have longer periods of activity followed by short periods of sleep in between.[41] Activity is scattered randomly throughout the day; aside from an avoidance of intense light, no regular circadian patterns are apparent.[41]
						<br>Domestic guinea pigs generally live in cages, although some owners of large numbers of guinea pigs dedicate entire rooms to their pets. Cages with solid or wire mesh floors are used, although wire mesh floors can cause injury and may be associated with an infection commonly known as bumblefoot (ulcerative pododermatitis).[59] "Cubes and Coroplast" (C&C) style cages are now a common choice.[60] Cages are often lined with wood shavings or a similar material. Bedding made from red cedar (Eastern or Western) and pine, both softwoods, were commonly used in the past, but these materials are now believed to contain harmful phenols (aromatic hydrocarbons) and oils.[61] Safer bedding materials made from hardwoods (such as aspen), paper products, and corn cob materials are other alternatives.[61] Guinea pigs tend to be messy within their cages; they often jump into their food bowls or kick bedding and feces into them, and their urine sometimes crystallizes on cage surfaces, making it difficult to remove.[62] After its cage has been cleaned, a guinea pig typically urinates and drags its lower body across the floor of the cage to mark its territory.[63] Male guinea pigs may also mark their territory in this way when they are taken out of their cages.</p>	
												
								
											
					<h4>Burrowing behavior</h4>
                               
									<p>
						All hamsters are excellent diggers, constructing burrows with one or more entrances, with galleries connected to chambers for nesting, food storage, and other activities.[2] They use their fore- and hindlegs, as well as their snouts and teeth, for digging. In the wild, the burrow buffers extreme ambient temperatures, offers relatively stable climatic conditions, and protects against predators. Syrian hamsters dig their burrows generally at a depth of 0.7 m.[16] A burrow includes a steep entrance pipe (4–5 cm in diameter), a nesting and a hoarding chamber and a blind-ending branch for urination. Laboratory hamsters have not lost their ability to dig burrows; in fact, they will do this with great vigor and skill if they are provided with the appropriate substrate.
						</p>	
									
								
							<h4>5. Diet</h4>
                               
									<p>
					The guinea pig's natural diet is grass; their molars are particularly suited for grinding plant matter and grow continuously throughout their life.[71] Most mammals that graze are large and have a long digestive tract; guinea pigs have much longer colons than most rodents, but they must also supplement their diet by eating their feces (coprophagy) .[72] However, they do not consume all their feces indiscriminately, but produce special soft pellets, called cecotropes (or caecal pellets), which recycle B vitamins, fiber, and bacteria required for proper digestion.[68][73] The cecotropes are eaten directly from the anus, unless the guinea pig is pregnant or obese.[68] They share this behaviour with rabbits. In geriatric boars or sows (rarely in young ones), the muscles which allow the softer pellets to be expelled from the anus can become weak. This creates a condition known as "anal impaction", which prevents the animal from redigesting cecotropes even though harder pellets may pass through the impacted mass.[74] The condition may be temporarily alleviated by a human carefully removing the impacted feces from the anus.
					<br>Guinea pigs benefit from a diet of fresh grass hay, such as timothy hay, in addition to food pellets which are often based on timothy hay. Alfalfa hay is also a popular food choice and most guinea pigs will eat large amounts of alfalfa when offered it,[21][75] though some controversy exists over offering alfalfa to adult guinea pigs. Some pet owners and veterinary organizations have advised that, as a legume rather than a grass hay, alfalfa consumed in large amounts may lead to obesity, as well as bladder stones from the excess calcium in all animals except for pregnant and very young guinea pigs.[76][77] However, published scientific sources mention alfalfa as a food source that can replenish protein, amino acids, and fiber</p>
								
							
									
							<h4>6. Reproduction</h4>
                               
									<p>
						Males reach sexual maturity in 3–5 weeks, while females can be fertile as early as 4 weeks old, and can carry litters before they are adults. The female guinea pig is able to breed year-round, with spring being the peak. A sow can have as many as five litters in a year, but six is theoretically possible.[20] Unlike the offspring of most rodents, which are altricial at birth, newborn cavy pups are well-developed with hair, teeth, claws, and partial eyesight.[44] The pups are immediately mobile and begin eating solid food immediately, though they continue to suckle. Females can once again become pregnant 6–48 hours after giving birth, but it is not healthy for a female to be constantly pregnant.<br>Females that do not give birth may develop an irreversible fusing of the pubic symphysis, a joint in the pelvis, due to calcification which may occur between 6 and 10 months of age. If they become pregnant after this has happened, the birth canal will probably not widen sufficiently and this may lead to dystocia and death as they attempt to give birth. Calcification of the female's pubic symphysis if not bred is greatly overstated. <br>
The gestation period lasts from 59 days (1.9 months) to 72 days (2.4 months), with an average of 63–68 days. Because of the long gestation period and the large size of the pups, pregnant females may become large and eggplant-shaped, although the change in size and shape varies depending upon the size of the litter. Litter size ranges from one to six, with three being the average;[90] the largest recorded litter size is 17.[91] The guinea pig mother only has two nipples, but she can readily raise the more average-sized litters of 2 to 4 pups.[92][93] In smaller litters, difficulties may occur during labour due to oversized pups. Large litters result in higher incidences of stillbirth, but because the pups are delivered at an advanced stage of development, lack of access to the mother's milk has little effect on the mortality rate of newborns.[94]
Cohabitating females assist in mothering duties if lactating; guinea pigs practice alloparental care, in which a female may adopt the pup(s) of another. This might take place if the original parents die or are for some reason separated from them. This behavior is common and is seen in many other animal species such as the elephant

Hamsters are born hairless and blind in a nest the mother will have prepared in advance.After one week, they begin to explore outside the nest. They are completely weaned after three weeks, or four for Roborovski hamsters.</p>		
								
								
								
								
								
										
							<h4>7. Health problems</h4>
                               
									<p>
				Common ailments in domestic guinea pigs include respiratory tract infections, diarrhea, scurvy (vitamin C deficiency, typically characterized by sluggishness), abscesses due to infection (often in the neck, due to hay embedded in the throat, or from external scratches), and infections by lice, mites, or fungus.<br>
 Mange mites (Trixacarus caviae) are a common cause of hair loss, and other symptoms may also include excessive scratching, unusually aggressive behavior when touched (due to pain), and, in some instances, seizures. Guinea pigs may also suffer from "running lice" (Gliricola porcelli), a small, white insect which can be seen moving through the hair; their eggs, which appear as black or white specks attached to the hair, are sometimes referred to as "static lice". Other causes of hair loss can be due to hormonal upsets caused by underlying medical conditions such as ovarian cysts
Foreign bodies, especially small pieces of hay or straw, can become lodged in the eyes of guinea pigs, resulting in excessive blinking, tearing, and in some cases an opaque film over the eye due to corneal ulcer. Hay or straw dust can also cause sneezing. While it is normal for guinea pigs to sneeze periodically, frequent sneezing may be a symptom of pneumonia, especially in response to atmospheric changes. Pneumonia may also be accompanied by torticollis and can be fatal. 
<br>	Because the guinea pig has a stout, compact body, it more easily tolerates excessive cold than excessive heat.  Its normal body temperature is 101–104 °F (38–40 °C),[111] so its ideal ambient air temperature range is similar to a human's, about 65–75 °F (18–24 °C).[110] Consistent ambient temperatures in excess of 90 °F (32 °C) have been linked to hyperthermia and death, especially among pregnant sows.Guinea pigs are not well suited to environments that feature wind or frequent drafts, and respond poorly to extremes of humidity outside of the range of 30–70%.
Guinea pigs are prey animals whose survival instinct is to mask pain and signs of illness, and many times health problems may not be apparent until a condition is severe or in its advanced stages. Treatment of disease is made more difficult by the extreme sensitivity guinea pigs have to most antibiotics, including penicillin, which kill off the intestinal flora and quickly bring on episodes of diarrhea and in some cases, death.<br>
 Similar to the inherited genetic diseases of other breeds of animal (such as hip dysplasia in canines), a number of genetic abnormalities of guinea pigs have been reported. Most commonly, the roan coloration of Abyssinian guinea pigs is associated with congenital eye disorders and problems with the digestive system. Other genetic disorders include "waltzing disease" (deafness coupled with a tendency to run in circles), palsy, and tremor conditions. 
		</p>		
								
	<h4>8. Handling, temperament and socialization</h4>								
<p>	If handled correctly early in life, guinea pigs become amenable to being picked up and carried, and seldom bite or scratch.[44] They are timid explorers and often hesitate to attempt an escape from their cage even when an opportunity presents itself.[67] Still, they show considerable curiosity when allowed to walk freely, especially in familiar and safe terrain. Guinea pigs that become familiar with their owner will whistle on the owner's approach; they will also learn to whistle in response to the rustling of plastic bags or the opening of refrigerator doors, where their food is most commonly stored. In Switzerland, owning a single guinea pig is considered harmful to its well-being and forbidden by law.					
	</p>							
											
