									<blockquote class="generic-blockquote">
					The red-eared slider (Trachemys scripta elegans), also known as the red-eared terrapin, is a semiaquatic turtle belonging to the family Emydidae. It is a subspecies of the pond slider. It is the most popular pet turtle in the United States and is also popular as a pet in the rest of the world.[2]It has, therefore, become the most commonly traded turtle in the world.[3] It is native to the southern United States and northern Mexico, but has become established in other places because of pet releases, and has become an invasive species in many areas, where it outcompetes native species. The red-eared slider is included in the list of the world's 100 most invasive species[4] published by the IUCN. </blockquote><br>
										<h4>
									1. Name
									</h4><br>
                                <img class="img-fluid" src="http://www.hdnicewallpapers.com/Walls/Big/Turtle/Beautiful_Slider_Turtle_Animal_Pics.jpg" alt="" style="height:400px;width:800px;"><br><br>
									<p>
									The red-eared slider get its name from the small red stripe around its ears and from its ability to slide quickly off rocks and logs into the water. This species was previously known as Troost's turtle in honor of an American herpetologist Gerard Troost. Trachemys scripta troostii is now the scientific name for another subspecies, the Cumberland slider</p>
										
										
										
										<h4>
									2. Taxonomy
									</h4><br>
                               
									<p>
							The red-eared slider belongs to the order Testudineswhich contains about 250 turtle species. It is a subspecies of Trachemys scripta. It was previously classified under the name Chrysemys scripta elegans.
The species Trachemys scripta contains three subspecies: T. s. elegans (red-eared slider), T. s. scripta (yellow-bellied slider), and T. s. troostii (Cumberland slider).[5]
</p>	
										
										
										
										<h4>
								3.Description
									</h4><br>
                               
									<p>
							The carapace of this species can reach more than 40 cm (16 in) in length, but the average length ranges from 15 to 20 cm (6 to 8 in).[6] The females of the species are usually larger than the males. They typically live between 20 and 30 years, although some individuals have lived for more than 40 years.[7] Their life expectancy is shorter when they are kept in captivity.[8] The quality of their living environment has a strong influence on their lifespans and well being.<br>
 

These turtles are poikilotherms, meaning they are unable to regulate their body temperatures independently; they are completely dependent on the temperature of their environment.[2] For this reason, they need to sunbathe frequently to warm themselves and maintain their body temperatures.
The shell is divided into two sections: the upper or dorsal carapace, and the lower, ventral carapace or plastron.[2]The upper carapace consists of the vertebral scutes, which form the central, elevated portion; pleural scutes that are located around the vertebral scutes; and then the marginal scutes around the edge of the carapace. The rear marginal scutes are notched. The scutes are bony keratinous elements. The carapace is oval and flattened (especially in the male) and has a weak keelthat is more pronounced in the young.[9] The color of the carapace changes depending on the age of the turtle. The carapace usually has a dark green background with light and dark, highly variable markings. In young or recently hatched turtles, it is leaf green and gets slightly darker as a turtle gets older, until it is a very dark green, and then turns a shade between brown and olive green. The plastron is always a light yellow with dark, paired, irregular markings in the centre of most scutes. The plastron is highly variable in pattern. The head, legs, and tail are green with fine, irregular, yellow lines. The whole shell is covered in these stripes and markings that aid in camouflaging an individual.<br>

Turtles also have a complete skeletal system, with partially webbed feet that help them to swim and that can be withdrawn inside the carapace along with the head and tail. The red stripe on each side of the head distinguishes the red-eared slider from all other North American species and gives this species its name, as the stripe is located behind the eyes where their (external) ears would be. These stripes may lose their color over time.[8] Some individuals can also have a small mark of the same color on the top of their heads. The red-eared slider does not have a visible outer ear or an external auditory canal; instead, it relies on a middle ear entirely covered by a cartilaginous tympanic disc.[10]
The main internal organs of these reptiles are the lungs, heart, stomach, liver, intestines, and the urinary bladder. A cloaca serves both excretory and reproductive functions

</p>	
										
											
										
										<h4>
								4. Sexual dimorphism
								</h4><br>
                               
									<p>
							Red-eared slider young look practically identical regardless of their sex, making it difficult to distinguish them. One useful method, however, is to inspect the markings under their carapace, which fade as the turtles age. It is much easier to distinguish the sex of adults, as the shells of mature males are smaller than those of females.[12] Male red-eared sliders reach sexual maturity when their carapaces' diameters measure 10 cm (3.9 in) and females reach maturity when their carapaces measure 15 cm. Both male and females reach sexual maturity at five to six years. The male is normally smaller than the female, although this parameter is sometimes difficult to apply as individuals being compared could be of different ages.<br>
Males have longer claws on their front feet than the females; this helps them to hold on to a female during mating and is used during courtship displays.[13] The male's tail is thicker and longer. Typically, the cloacal opening of the female is at or under the rear edge of the carapace, while the male's opening occurs beyond the edge of the carapace. The male's plastron is slightly concave, while that of the female is completely flat. The male's concave plastron also helps to stabilize the male on the female's carapace during mating.[14] Older males can sometimes have a dark greyish-olive green melanistic coloration, with very subdued markings. The red stripe on the sides of the head may be difficult to see or be absent. The female's appearance is substantially the same throughout her life.
</p>	
										
										
								
										<h4>
								5. Distribution and habitat
									</h4><br>
								
                               
									<p>
				The red-eared slider originated from the area around the Mississippi River and the Gulf of Mexico, in warm climates in the southeastern United States. Their native areas range from the southeast of Colorado to Virginia and Florida. In nature, they inhabit areas with a source of still, warm water, such as ponds, lakes, swamps, creeks, streams, or slow-flowing rivers. They live in areas of calm water where they are able to leave the water easily by climbing onto rocks or tree trunks so they can warm up in the sun. Individuals are often found sunbathing in a group or even on top of each other. They also require abundant aquatic plants, as these are the adults' main food, although they are omnivores.[15] Turtles in the wild always remain close to water unless they are searching for a new habitat or when females leave the water to lay their eggs.<br>
Owing to their popularity as pets, red-eared sliders have been released or escaped into the wild in many parts of the world.[16] The turtle is considered one of the world’s worst invasive species.[17] Feral populations are now found in Australia, Europe, South Africa, the Caribbean Islands, Israel, Bahrain, the Mariana Islands, Guam, and southeast and far-east Asia.[18][19] In Australia, it is illegal for members of the public to import, keep, trade, or release red-eared sliders, as they are regarded as an invasive species[20] - see below. Their import has been banned by the European Union[21] as well as specific EU member countries.[22] In 2015 Japan announced it was planning to ban the import of red-eared sliders, but it would probably not take effect until 2020.[23] Invasive red-eared sliders cause negative impacts in the ecosystems they occupy because they have certain advantages over the native populations, such as a lower age at maturity, higher fecundity rates, and larger body size, which gives them a competitive advantage at basking and nesting sites, as well as when exploiting food resources.[24] They also transmit diseases and displace the other turtle species with which they compete for food and breeding space.
</p>			
								
					<h4>6. Behavior</h4>
                               
									<p>
			Red-eared sliders are almost entirely aquatic, but as they are cold-blooded, they leave the water to sunbathe to regulate their temperature.</p>		
								
							<h4>7. Hibernation</h4>
                               
									<p>
					Red-eared sliders do not hibernate, but actually brumate; while they become less active, they do occasionally rise to the surface for food or air. Brumation can occur to varying degrees. In the wild, red-eared sliders brumate over the winter at the bottoms of ponds or shallow lakes. They generally become inactive in October, when temperatures fall below 10 °C (50 °F).[9] During this time, the turtles enter a state of sopor, during which they do not eat or defecate, they remain nearly motionless, and the frequency of their breathing falls. Individuals usually brumate underwater, but they have also been found under banks and rocks, and in hollow stumps. In warmer winter climates, they can become active and come to the surface for basking. When the temperature begins to drop again, however, they quickly return to a brumation state. Sliders generally come up for food in early March to as late as the end of April</p>				
							<h4>8. Reproduction</h4>
                               
									<p>
					Courtship and mating activities for red-eared sliders usually occur between March and July, and take place under water. During courtship, the male swims around the female and flutters or vibrates the back side of his long claws on and around her face and head, possibly to direct pheromonestowards her.[29] The female swims toward the male and, if she is receptive, sinks to the bottom for mating. If the female is not receptive, she may become aggressive towards the male. Courtship can last 45 minutes, but mating takes only 10 minutes.<br>
 

On occasion, a male may appear to be courting another male, and when kept in captivity may also show this behaviour towards other household pets. Between male turtles, it could be a sign of dominance and may preclude a fight. Young turtles may carry out the courtship dance before they reach sexual maturity at five years of age, but they are unable to mate.<br>
After mating, the female spends extra time basking to keep her eggs warm.[30] She may also have a change of diet, eating only certain foods, or not eating as much as she normally would. A female can lay between two and 30 eggs depending on body size and other factors.[18] One female can lay up to five clutches in the same year, and clutches are usually spaced 12 to 36 days apart.[31] The time between mating and egg-laying can be days or weeks. The actual egg fertilization takes place during the egg-laying. This process also permits the laying of fertile eggs the following season, as the sperm can remain viable and available in the female's body in the absence of mating. During the last weeks of gestation, the female spends less time in the water and smells and scratches at the ground, indicating she is searching for a suitable place to lay her eggs. The female excavates a hole, using her hind legs, and lays her eggs in it.<br>
Incubation takes 59 to 112 days.[18] Late-season hatchlings may spend the winter in the nest and emerge when the weather warms in the spring. Just prior to hatching, the egg contains 50% turtle and 50% egg sac. A new hatchling breaks open its egg with its egg tooth, which falls out about an hour after hatching. This egg tooth never grows back. Hatchlings may stay inside their eggshells after hatching for the first day or two. If they are forced to leave the eggshell before they are ready, they will return if possible. When a hatchling decides to leave the shell, it still has a small sac protruding from its plastron. The yolk sac is vital and provides nourishment while visible, and several days later it will have been absorbed into the turtle's belly. The sac must be absorbed, and does not fall off. The split must heal on its own before the turtle is able to swim. The time between the egg hatching and water entry is 21 days.
Damage to or inordinate motion of the protruding egg yolk, enough to allow air into the turtle's body, results in death. This is the main reason for marking the top of turtle eggs if their relocation is required for any reason. An egg turned upside down will eventually terminate the embryo's growth by the sac smothering the embryo. If it manages to reach term, the turtle will try to flip over with the yolk sac, which would allow air into the body cavity and cause death. The other fatal danger is water getting into the body cavity before the sac is absorbed completely and while the opening has not completely healed yet.<br>
The sex of red-eared sliders is determined by the incubation temperature during critical phases of the embryos' development. Only males are produced when eggs are incubated at temperatures of 22–27 °C (72–81 °F), whereas females develop at warmer temperatures.[33]Colder temperatures result in the death of the embryos.
</p>		
								
								
								
								
										
							<h4>9. US state law</h4>
                               
									<p>
			Some states have other laws and regulations regarding possession of red-eared sliders because they can be an invasive species where they are not native and have been introduced through the pet trade. Now, it is illegal in Florida to sell any wild-type red-eared slider, as they interbreed with the local yellow-bellied slider population — Trachemys scripta scripta is another subspecies of pond sliders, and hybrids typically combine the markings of the two subspecies. However, unusual color varieties such as albino and pastel red-eared sliders, which are derived from captive breeding, are still allowed for sale</p>		
								
	<h4>10. An invasive species in Australia</h4>							<br>
<p>	In Australia, breeding populations have been found in New South Wales and Queensland, and individual turtles have been found in the wild in Victoria, the Australian Capital Territory and Western Australia.<br>
Red-eared slider turtles are considered a significant threat to native turtle species – they mature more quickly, grow larger, produce more offspring and are more aggressive.[42] Numerous studies provide evidence that red-eared slider turtles can out-compete native turtles for food and nesting and basking sites.[43] Because red-eared slider turtles eat plants as well as animals, they could also have a negative impact on a range of native aquatic species, including rare frogs.[44] There is also a significant risk that red-eared slider turtles will transfer diseases and parasites to native reptile species. There is evidence that a malaria-like parasite was spread to two wild turtle populations in Lane Cove River, Sydney.<br>
Social and economic costs are also likely to be substantial. The Queensland government has invested close to $1 million AUD in eradication programs to date.[42] As outlined in the foregoing section, the turtle may also have significant public health costs due to the impacts of turtle-associated salmonella on human health. Outbreaks in multiple states and fatalities in children, associated with handling salmonella-infected turtles, have been recorded in the USA. Salmonella can also spread to humans when turtles contaminate drinking water.<br>
There have been varying degrees of action by state governments to date, ranging from ongoing eradication efforts by the Queensland government to very little action by the government of New South Wales.[48] Experts have ranked the species as high priority for management in Australia and are calling for a national prevention and eradication strategy, including a concerted education and compliance program to stop the illegal trade, possession and release of slider turtles.

 
